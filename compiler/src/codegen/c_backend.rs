fn bin_op_str(op: BinOp) -> &'static str {
    match op {
        BinOp::Add => " + ",
        BinOp::Sub => " - ",
        BinOp::Mul => " * ",
        BinOp::Div => " / ",
        BinOp::Mod => " % ",
        BinOp::Eq => " == ",
        BinOp::NotEq => " != ",
        BinOp::Lt => " < ",
        BinOp::Le => " <= ",
        BinOp::Gt => " > ",
        BinOp::Ge => " >= ",
    }
}

/// Render `s` as a C double-quoted string literal. UTF-8 bytes outside
/// printable ASCII are emitted as `\xNN`, which works because mott source
/// is UTF-8 and we never split a multibyte sequence.
fn c_string_literal(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for b in s.bytes() {
        match b {
            b'\\' => out.push_str("\\\\"),
            b'"' => out.push_str("\\\""),
            b'\n' => out.push_str("\\n"),
            b'\t' => out.push_str("\\t"),
            b'\r' => out.push_str("\\r"),
            0x20..=0x7e => out.push(b as char),
            _ => {
                // Hex-escape followed by a string-break prevents the next ASCII
                // digit from being absorbed into the \x escape.
                let _ = write!(out, "\\x{:02x}\"\"", b);
            }
        }
    }
    out.push('"');
    out
}

